                <h2> What is the car</h2>
                <P style="color: white;">A car, or an automobile, is a motor vehicle with wheels. Most definitions of cars say that they
                     run primarily on roads, seat one 
                    to eight people, have four wheels, and mainly transport people, not cargo<br><br>

                    French inventor Nicolas-Joseph Cugnot built the first steam-powered road vehicle in 1769, while French-born Swiss inventor
                     François Isaac de Rivaz designed and constructed the first internal combustion-powered automobile in 1808.<br><br> The modern
                      car—a practical, marketable automobile for everyday use—was invented in 1886, when German inventor Carl Benz patented his 
                      Benz Patent-Motorwagen. 
                      <br><br>
                      Commercial cars became widely available during the 20th century. One of the first cars affordable
                       by the masses was the 1908 Model T, an American car manufactured by the Ford Motor Company. Cars were rapidly adopted in the US, 
                       where they replaced horse-drawn carriages. In Europe and other parts of the world, demand for
                     automobiles did not increase until after World War II. The car is considered an essential part of the developed economy.</P>
              
              
            
